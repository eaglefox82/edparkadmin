     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $e = New Stock();
        $e->item = $request->get('item');
        $e->qty = $request->get('qty');
        $e->cost = $request->get('cost');
        $e->location = $request->get('location');
        $e->current = 'Y';
        $e->save();

        Alert::Success("Item Added")->autoclose(1500);
        return redirect(action('StockController@index'));

    }

